class ShiftOperatorDemo{
public static void main(String args[])
{
int a=20;
int b=40;
System.out.println("operator left shift:a<<2="+(a<<2));
System.out.println("operator right shift:a>>2="+(a>>2));
System.out.println("operator left shift:b<<2="+(b<<2));
System.out.println("operator left shift:b>>2="+(b>>2));
}
}

